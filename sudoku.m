function Save_Callback(hObject, eventdata, handles)
% hObject    handle to Save (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
File = uiputfile({'*.sud','Sudoku-file'},'Save Sudoku As');
f = fopen(File,'wt');
global sud;
for i=1:9
    for j=1:9
        if strcmp(get(sud(i,j),'String'),'')
            fprintf(f, '0 ');
        else
            fprintf(f, '%s ', get(sud(i,j),'String'));
        end  
        if strcmp(get(sud(i,j),'Enable'), 'on')
            fprintf(f,'1 ');
        else
            fprintf(f,'0 ');
        end
    end
    fprintf(f,'\n');
end
fprintf(f,'---Solution---\n');
global solution;
for i=1:9
    for j=1:9
        fprintf(f, '%s ', solution(i,j));
    end
    fprintf(f,'\n');
end
fclose(f);


