function Load_Callback(hObject, eventdata, handles)
% hObject    handle to Load (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
File = uigetfile({'*.sud','Sudoku-file'},'Save Sudoku As');
f = fopen(File,'rt');
global sud;
global solution;
for i=1:9
    for j=1:9
        t= fscanf(f,'%s ',1);
        if t=='0'
            set(sud(i,j),'String','');
        else
            set(sud(i,j),'String',t);
        end
        t = fscanf(f,'%d ',1);
        if t == 0 
            set(sud(i,j),'Enable','off');
        else
            if t == 1
                set(sud(i,j),'Enable','on');
            end
        end
    end
    fscanf(f,'\n');
end
fscanf(f,'---Solution---');
for i=1:9
    for j=1:9
        solution(i,j) = fscanf(f,'%d ',1);
    end
    fscanf(f,'\n');
end

fclose(f);
