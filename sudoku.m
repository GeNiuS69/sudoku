function New_Callback(hObject, eventdata, handles)
% hObject    handle to New (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
generate;

global sud;
global solution;
for i=1:9
    for j=1:9
        solution(i,j)=get(sud(i,j),'String');
    end
end
block();
difficult = round(rand(1)*20)+20;
for k=1:difficult
    x = round(rand(1)*8)+1;
    y = round(rand(1)*8)+1;
    while (get(sud(x,y),'String')~='')
        x = round(rand(1)*8)+1;
        y = round(rand(1)*8)+1;
    end
    set(sud(x,y),'String','');
    set(sud(x,y),'Enable','on');
end


function block
global sud;
for i=1:9
    for j=1:9
        set(sud(i,j),'Enable','off');
    end
end

function generate_basic_table
global sud;
for i=0:8
    for j=0:8
        set(sud(i+1,j+1),'String',mod(i*3+fix(i/3)+j,9)+1);
    end
end

function swap_rows_small
global sud;
area = round(rand(1)*2);
line1 = round(rand(1)*2);
N1 = area * 3 + line1+1;
line2 = round(rand(1)*2);
while line1==line2
    line2 = round(rand(1)*2);
end
N2 = area * 3 + line2+1;
swap_two_rows(N1,N2);

function swap_columns_small
global sud;
sud = transpose(sud);
swap_rows_small;
sud = transpose(sud);

function swap_rows_area
global sud;
area1 = round(rand(1)*2);
area2 = round(rand(1)*2);
while area1==area2
    area2 = round(rand(1)*2);
end;
swap_two_rows(area1*3+1,area2*3+1);
swap_two_rows(area1*3+2,area2*3+2);
swap_two_rows(area1*3+3,area2*3+3);
function swap_columns_area
global sud;
sud = transpose(sud);
swap_rows_area;
sud = transpose(sud);


function generate
generate_basic_table;
for i=1:round(rand(1)*10)
    swap_rows_small;
end;
for i=1:round(rand(1)*10)
    swap_columns_small;
end;
for i=1:round(rand(1)*10)
    swap_rows_area;
end;
for i=1:round(rand(1)*10)
    swap_columns_area;
end;

function swap_two_rows(k,m)
global sud;
for i=1:9
    tmp=get(sud(k,i),'String');
    set(sud(k,i),'String',get(sud(m,i),'String'));
    set(sud(m,i),'String',tmp);
end


